// Class declaration
public class Variables {
  // Main method
  public static void main(String[] args) {
      // Declare and initialize variables
      String name = "Tony Stark"; // String variable
      int age = 48; // Integer variable
      double price = 34.44; // Double variable
      int a = 23; // Integer variable
      int b = 43; // Integer variable

      // Update the value of variable b
      b = 20;

      // Update the value of variable name
      name = "Ironman";

      // Print variables
      System.out.println("Name: " + name);
      System.out.println("Age: " + age);
      System.out.println("Price: " + price);
      System.out.println("a: " + a);
      System.out.println("b: " + b);
  }
}
