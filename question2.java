import java.util.*;

// Class declaration
public class Question2 {
    // Main method
    public static void main(String[] args) {
        // Scanner object for input
        Scanner sc = new Scanner(System.in);
        
        // Reading two integers
        int a =  sc.nextInt();
        int b = sc.nextInt();
        
        // Calculating sum and printing
        int sum = a + b;
        System.out.println(sum);
        
        // Closing scanner
        sc.close();
    }
}
